package string_prgms;

public class Unary {

	public static void main(String[] args) {
		int x=4,y=4;
		System.out.println(x);
		System.out.println(x++);
		System.out.println(x);
		System.out.println(++x);
		System.out.println(x);
		System.out.println(y);
		System.out.println(y--);
		System.out.println(y);
		System.out.println(--y);
		System.out.println(y);
		

	}

}
